    {
        int mid = min + (max - min) / 2;

        if (arr[mid] == xx)
        {
            found = arr[mid];
            fnd = true;
            break;
        }

        if (arr[mid] > xx)
        {
            max = mid - 1;
        }
        else
        {
            min = mid + 1;
        }
    }
