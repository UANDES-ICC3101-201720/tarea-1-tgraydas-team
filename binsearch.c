void serial_binsearch(unsigned int arr[], int l, int r, int x)
{
    int low, high, mid;

    low = l;
    high = r - 1;

    while (low < high)
    {
        mid = (low + high) / 2;

        if (x < mid)
            high = mid - 1;
        else if (x > mid)
            low = mid + 1;
        else
        {
            printf("[binsearch] Se enctontro el numero con valor %u\n", arr[mid]);
            break;
        }
    }
}

